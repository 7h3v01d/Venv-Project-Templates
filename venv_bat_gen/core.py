class PresetManager:
    """Loads and saves user presets to ~/.keystoneai/venv_generator_presets.json.

    Built-in presets are always available and cannot be overwritten or deleted.
    User presets are stored in the JSON file and merged on top of builtins.
    """

    _STORAGE_DIR  = Path.home() / ".keystoneai"
    _STORAGE_FILE = _STORAGE_DIR / "venv_generator_presets.json"

    def __init__(self) -> None:
        self._user: dict[str, dict] = {}
        self._load()

    def names(self) -> list[str]:
        return sorted(_BUILTIN_PRESETS) + sorted(self._user)

    def get(self, name: str) -> dict | None:
        return _BUILTIN_PRESETS.get(name) or self._user.get(name)

    def is_builtin(self, name: str) -> bool:
        return name in _BUILTIN_PRESETS

    def save(self, name: str, data: dict) -> None:
        if name in _BUILTIN_PRESETS:
            raise ValueError(f'"{name}" is a built-in preset and cannot be overwritten.')
        self._user[name] = {k: data[k] for k in _PRESET_FIELDS if k in data}
        self._persist()

    def delete(self, name: str) -> None:
        if name in _BUILTIN_PRESETS:
            raise ValueError(f'"{name}" is a built-in preset and cannot be deleted.')
        if name not in self._user:
            raise KeyError(name)
        del self._user[name]
        self._persist()

    def _load(self) -> None:
        if self._STORAGE_FILE.exists():
            try:
                raw = json.loads(self._STORAGE_FILE.read_text(encoding="utf-8"))
                if isinstance(raw, dict):
                    self._user = raw
            except (json.JSONDecodeError, OSError):
                self._user = {}

    def _persist(self) -> None:
        try:
            self._STORAGE_DIR.mkdir(parents=True, exist_ok=True)
            self._STORAGE_FILE.write_text(
                json.dumps(self._user, indent=2), encoding="utf-8"
            )
        except OSError as exc:
            print(f"[PresetManager] Could not save presets: {exc}", file=sys.stderr)
