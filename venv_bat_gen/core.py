# Valid Python module/package name: identifiers separated by dots
MODULE_RE = re.compile(r"^[A-Za-z_][A-Za-z0-9_]*(\.[A-Za-z_][A-Za-z0-9_]*)*$")

# Characters that break cmd.exe control flow when unquoted
RUNNER_ARGS_UNSAFE_RE = re.compile(r'[&|<>^"]')


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def bat_escape(value: str) -> str:
    """Escape characters that would break batch variable assignments."""
    value = value.replace('"', "")
    value = value.replace("%", "%%")
    return value


# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

@dataclass(frozen=True)
class GeneratorConfig:
    project_dir:           Path
    project_name:          str
    venv_dir:              str
    entry_mode:            str   # "file" | "module" | "runner"
    app_entry:             str
    runner_args:           str   # runner mode only
    overwrite_existing:    bool
    create_requirements:   bool
    include_webengine_check: bool
    pause_on_exit:         bool
    create_venv_now:       bool
    include_test_bat:      bool
    use_uv:                bool  # use uv instead of pip where possible
    include_posix:         bool  # also generate POSIX .sh equivalents


