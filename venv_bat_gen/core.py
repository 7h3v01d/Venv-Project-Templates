_UV_INDICATORS = ("[tool.uv]", "uv.lock", "[build-system]")


def scan_project_folder(folder: Path, venv_dir: str = ".venv") -> FolderScan:
    """Inspect folder and return a FolderScan. Pure function, no side effects."""
    hints: list[str] = []

    # venv
    venv_path  = folder / venv_dir
    venv_found = (venv_path / "Scripts" / "python.exe").exists() or \
                 (venv_path / "bin" / "python").exists()
    hints.append(
        f"✔  Existing venv found: {venv_dir}/" if venv_found
        else f"·  No venv at {venv_dir}/ — will be created on generate"
    )

    # requirements / project files
    has_requirements = (folder / "requirements.txt").exists()
    has_pyproject    = (folder / "pyproject.toml").exists()
    has_setup_py     = (folder / "setup.py").exists() or (folder / "setup.cfg").exists()
    has_uv_lock      = (folder / "uv.lock").exists()

    if has_requirements: hints.append("✔  requirements.txt found")
    if has_pyproject:    hints.append("✔  pyproject.toml found")
    if has_setup_py:     hints.append("✔  setup.py / setup.cfg found")
    if has_uv_lock:      hints.append("✔  uv.lock found")

    # read pyproject.toml for hints
    pyproject_text = ""
    if has_pyproject:
        try:
            pyproject_text = (folder / "pyproject.toml").read_text(encoding="utf-8").lower()
        except OSError:
            pass

    # uv detection
    suggested_use_uv = has_uv_lock or "[tool.uv]" in pyproject_text
    if suggested_use_uv:
        hints.append("→  uv project detected — suggesting uv mode")

    # POSIX suggestion — recommend .sh scripts when not on Windows
    suggested_use_posix = sys.platform != "win32"
    if suggested_use_posix:
        hints.append("→  Non-Windows platform detected — suggesting POSIX .sh scripts")

    # entry point: [project.scripts] first
    suggested_mode:  str | None = None
    suggested_entry: str | None = None
    suggested_args:  str | None = None

    scripts_match = re.search(r"\[project\.scripts\](.*?)(\[|\Z)", pyproject_text, re.DOTALL)
    if scripts_match:
        entry_match = re.search(r'=\s*"([^:"\s]+)', scripts_match.group(1))
        if entry_match:
            module_path    = entry_match.group(1).split(":")[0]
            suggested_mode  = "module"
            suggested_entry = module_path
            suggested_args  = ""
            hints.append(f"→  Entry point from [project.scripts]: {module_path}")

    # dependency keyword scan
    if suggested_mode is None and pyproject_text:
        for keyword, mode, entry, args in _PYPROJECT_HINTS:
            if keyword in pyproject_text:
                suggested_mode  = mode
                suggested_entry = entry
                suggested_args  = args
                hints.append(f"→  Detected {keyword} — suggesting {mode} mode: {entry}")
                break

    # common entry file fallback
    if suggested_mode is None:
        for candidate in ("main.py", "app.py", "__main__.py", "run.py", "cli.py"):
            if (folder / candidate).exists():
                suggested_mode  = "file"
                suggested_entry = candidate
                suggested_args  = ""
                hints.append(f"→  Found {candidate} — suggesting file mode")
                break

    return FolderScan(
        venv_found=venv_found,
        venv_path=venv_path if venv_found else None,
        has_requirements=has_requirements,
        has_pyproject=has_pyproject,
        has_setup_py=has_setup_py,
        has_uv_lock=has_uv_lock,
        suggested_entry_mode=suggested_mode,
        suggested_app_entry=suggested_entry,
        suggested_runner_args=suggested_args,
        suggested_use_uv=suggested_use_uv,
        suggested_use_posix=suggested_use_posix,
        hints=hints,
    )


