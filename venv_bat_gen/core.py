def generate_files(cfg: GeneratorConfig) -> list[Path]:
    """Write all scripts to cfg.project_dir and return list of written paths."""
    import os as _os
    import stat as _stat

    cfg.project_dir.mkdir(parents=True, exist_ok=True)
    outputs = build_previews(cfg)

    written: list[Path] = []
    for filename, content in outputs.items():
        path = cfg.project_dir / filename
        if path.exists() and not cfg.overwrite_existing:
            raise FileExistsError(
                f"{path.name} already exists in that folder.\n"
                "Enable 'Overwrite existing files' to replace it."
            )
        is_sh = filename.endswith(".sh")
        # .sh files: Unix LF line endings; .bat files: Windows CRLF
        path.write_text(content, encoding="utf-8", newline="\n" if is_sh else "\r\n")
        if is_sh:
            # make executable (owner+group+other execute bits)
            current = _os.stat(path).st_mode
            _os.chmod(path, current | _stat.S_IXUSR | _stat.S_IXGRP | _stat.S_IXOTH)
        written.append(path)

    if cfg.create_requirements:
        req = cfg.project_dir / "requirements.txt"
        if not req.exists():
            req.write_text("", encoding="utf-8")
            written.append(req)

    return written


def create_venv(cfg: GeneratorConfig) -> None:
    """Create a venv at cfg.project_dir / cfg.venv_dir using uv or stdlib.

    If use_uv is True but uv is not installed, falls back to python -m venv
    rather than crashing — matching the graceful behaviour of the generated scripts.
    """
    import shutil as _shutil
    venv_path = cfg.project_dir / cfg.venv_dir
    if venv_path.exists():
        return
    if cfg.use_uv and _shutil.which("uv"):
        subprocess.run(["uv", "venv", str(venv_path)], cwd=str(cfg.project_dir), check=True)
    else:
        if cfg.use_uv:
            print(
                "[WARN] uv not found on PATH — falling back to python -m venv",
                file=sys.stderr,
            )
        subprocess.run(
            [sys.executable, "-m", "venv", str(venv_path)],
            cwd=str(cfg.project_dir),
            check=True,
        )


